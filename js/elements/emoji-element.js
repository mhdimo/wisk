    hide() {
        this.shadowRoot.querySelector('.outer').classList.remove('show');
        this.shadowRoot.querySelector('.inner').classList.remove('show');
    }

    searchEmojis(query) {
        const searchTerm = query.toLowerCase();
        const results = this.allEmojis.filter(
            emoji => emoji.name.toLowerCase().includes(searchTerm) || emoji.emoji.toLowerCase().includes(searchTerm)
        );

        return results;
    }

    searchDiscordEmojis(query) {
        const searchTerm = query.toLowerCase();
        const results = this.allEmojis.filter(emoji => emoji.discordShortcut.toLowerCase().includes(searchTerm));

        return results;
    }

    dispatch(emoji) {
        window.dispatchEvent(new CustomEvent('emoji-selector', { detail: { id: this.givenId, emoji: emoji } }));
        this.shadowRoot.querySelector('.outer').classList.remove('show');
        this.shadowRoot.querySelector('.inner').classList.remove('show');
        console.log('Dispatched emoji: ' + emoji, 'to id: ' + this.givenId);
        // copy to clipboard
        navigator.clipboard.writeText(emoji);
        wisk.utils.showToast('Emoji copied to clipboard', 3000);
    }

    onSearch(e) {
        const searchTerm = e.target.value.toLowerCase();
        if (!searchTerm) {
            this.searchResults = [];
            this.requestUpdate();
            return;
        }

        // Search through all categories
        this.searchResults = [
            ...this.smileysAndEmotion.filter(
                emoji => emoji.name.toLowerCase().includes(searchTerm) || emoji.emoji.toLowerCase().includes(searchTerm)
            ),
            ...this.peopleAndBody.filter(emoji => emoji.name.toLowerCase().includes(searchTerm) || emoji.emoji.toLowerCase().includes(searchTerm)),
            ...this.animalsAndNature.filter(emoji => emoji.name.toLowerCase().includes(searchTerm) || emoji.emoji.toLowerCase().includes(searchTerm)),
            ...this.foodAndDrink.filter(emoji => emoji.name.toLowerCase().includes(searchTerm) || emoji.emoji.toLowerCase().includes(searchTerm)),
            ...this.travelAndPlaces.filter(emoji => emoji.name.toLowerCase().includes(searchTerm) || emoji.emoji.toLowerCase().includes(searchTerm)),
            ...this.activities.filter(emoji => emoji.name.toLowerCase().includes(searchTerm) || emoji.emoji.toLowerCase().includes(searchTerm)),
            ...this.objects.filter(emoji => emoji.name.toLowerCase().includes(searchTerm) || emoji.emoji.toLowerCase().includes(searchTerm)),
            ...this.symbols.filter(emoji => emoji.name.toLowerCase().includes(searchTerm) || emoji.emoji.toLowerCase().includes(searchTerm)),
            ...this.flags.filter(emoji => emoji.name.toLowerCase().includes(searchTerm) || emoji.emoji.toLowerCase().includes(searchTerm)),
        ];

        this.requestUpdate();
    }

    randomEmoji() {
        const randomIndex = Math.floor(Math.random() * this.allEmojis.length);
        return this.allEmojis[randomIndex].emoji;
    }

    onKeyDown(e) {
        if (e.key === 'Enter') {
            const firstEmoji = this.getFirstVisibleEmoji();
            if (firstEmoji) {
                this.dispatch(firstEmoji.emoji);
            }
        }
        if (e.key === 'Escape') {
            this.hide();
        }
    }

    getFirstVisibleEmoji() {
        if (this.searchResults.length > 0) {
            return this.searchResults[0];
        } else if (this.selectedCategory.length > 0) {
            return this.selectedCategory[0];
        }
        return null;
    }

